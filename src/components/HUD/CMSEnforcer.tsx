// ---------------------------------------------------------------------------

function generateDefaultNotice(submission: ActiveSubmission): string {
  const date = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
  const deadlineDate = submission.deadlineAt.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
  const hoursOverdue = Math.floor(
    (Date.now() - submission.deadlineAt.getTime()) / (1000 * 60 * 60)
  );

  return `FORMAL REGULATORY NOTICE OF DEFAULT & NON-COMPLIANCE
Pursuant to CMS-0057-F Final Rule (42 CFR Parts 422, 438, 456, 457, 476, 482)

Date: ${date}
To: ${submission.payerName} — Medical Director & Compliance Officer
Re: Statutory Deadline Violation — Prior Authorization #MED-${submission.id.toUpperCase()}

NOTICE OF DEFAULT:

This letter constitutes formal notice that ${submission.payerName} has failed to comply with the mandatory prior authorization response deadlines established under CMS-0057-F.

CASE DETAILS:
• Procedure: ${submission.procedureName} (CPT ${submission.cptCode})
• Submission Date: ${submission.submittedAt.toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' })}
• Statutory Deadline: ${deadlineDate} (${submission.submissionType === '72h' ? '72 hours — Urgent' : '7 calendar days — Standard'} per CMS-0057-F)
• Current Status: ${hoursOverdue} HOURS PAST DEADLINE — DEFAULT

REGULATORY VIOLATION:

1. CMS-0057-F § 422.138(f) — Failure to provide timely determination
2. 42 CFR § 422.568 — Failure to meet mandatory PA response timeframes
3. State Prompt-Pay Statute (applicable state law) — Constructive approval may apply

REMEDIES DEMANDED:

(a) IMMEDIATE approval of the above-referenced prior authorization request as constructively granted per statutory default;
(b) Written confirmation of approval within 24 hours;
(c) Explanation of cause for delay and corrective action plan to prevent recurrence.

Failure to comply within 5 business days will result in immediate escalation to:
• Centers for Medicare & Medicaid Services (CMS)
• State Insurance Commissioner
• Office of Inspector General (OIG)
• ERISA Plan Administrator (if applicable)

This notice is auto-generated by MedHero PriorAuth AI CMS-0057-F Enforcer and constitutes formal regulatory correspondence.

— MedHero PriorAuth AI Compliance System`;
}

// ---------------------------------------------------------------------------
// Countdown timer component
// ---------------------------------------------------------------------------

function CountdownTimer({ deadline, isOverdue }: { deadline: Date; isOverdue: boolean }) {
  const [timeLeft, setTimeLeft] = useState('');

  useEffect(() => {
    const update = () => {
      const now = Date.now();
      const diff = deadline.getTime() - now;

      if (diff <= 0) {
        const overdueMs = Math.abs(diff);
        const hours = Math.floor(overdueMs / (1000 * 60 * 60));
        const mins = Math.floor((overdueMs % (1000 * 60 * 60)) / (1000 * 60));
        const secs = Math.floor((overdueMs % (1000 * 60)) / 1000);
        setTimeLeft(`+${hours}h ${mins}m ${secs}s OVERDUE`);
      } else {
        const hours = Math.floor(diff / (1000 * 60 * 60));
        const mins = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
        const secs = Math.floor((diff % (1000 * 60)) / 1000);
        if (hours > 0) {
          setTimeLeft(`${hours}h ${mins}m ${secs}s remaining`);
        } else if (mins > 0) {
          setTimeLeft(`${mins}m ${secs}s remaining`);
        } else {
          setTimeLeft(`${secs}s remaining`);
        }
      }
    };

    update();
    const interval = setInterval(update, 1000);
    return () => clearInterval(interval);
  }, [deadline]);

  return (
    <span
      className={`text-xs font-mono font-bold tabular-nums ${
        isOverdue ? 'text-status-red animate-pulse' : 'text-text-primary'
      }`}
    >
      {timeLeft}
    </span>
  );
}

